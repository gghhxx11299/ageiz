def get_translation(key: str, lang: str) -> str:
    """
    Get a translated string by key and language.
    Falls back to English if translation not found.
    """
    if not lang:
        lang = "english"

    lang_lower = lang.lower()

    # Search through all translation categories
    for category in TRANSLATIONS.values():
        if key in category:
            translations = category[key]
            # Try exact language match first
            if lang_lower in translations:
                return translations[lang_lower]
            # Fall back to English
            if "english" in translations:
                return translations["english"]
            # Last resort: return first available translation
            for t in translations.values():
                return t

    # Return key as last resort
    return key
